CREATE TABLE GRT_COMMANDS (
  ID VARCHAR(36) NOT NULL PRIMARY KEY,
  TYPE VARCHAR(128) NOT NULL,
  CONTEXT VARCHAR(64) NOT NULL,
  NAME VARCHAR(128) NOT NULL,
  VERSION INTEGER,
  STATUS VARCHAR(16) NOT NULL,
  ISSUED_AT TIMESTAMP NOT NULL,
  FORWARDED_AT TIMESTAMP,
  STARTED_AT TIMESTAMP,
  FINISHED_AT TIMESTAMP,
  PROCESSED_AT TIMESTAMP,
  TRIGGERED_BY_EVENT VARCHAR(36),
  ISSUED_BY_COMMAND VARCHAR(36),
  CORRELATED_BY_KEY VARCHAR(128) NOT NULL,
  CORRELATED_TO_EVENTS VARCHAR(4096),
  CORRELATED_TO_TOKEN VARCHAR(128),
  RESULTING_IN VARCHAR(40),
  PROBLEM_CODE VARCHAR(128),
  PROBLEM_MESSAGE VARCHAR(1024),
  PROBLEM_OCCURED_AT TIMESTAMP,
  JSON TEXT NOT NULL
)
